package com.example.fessutival_re;

public class Event {
    private String name;

    // 생성자
    public Event(String name) {
        this.name = name;
    }

    // Getter 메서드
    public String getName() {
        return name;
    }

    // Setter 메서드 (필요에 따라 추가)
    public void setName(String name) {
        this.name = name;
    }
}
